//Logic: Compare characters from both ends moving to the center.

bool isPalindrome(string s) {
    int left = 0, right = s.length() - 1;
    while (left < right) {
        if (s[left] != s[right]) return false;
        left++;
        right--;
    }
    return true;
}

// Time Complexity: O(n)


// Space Complexity: O(1)
